    private void tampildata(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("barang");
        tbl.addColumn("kode");
        tbl.addColumn("harga");
        tbl.addColumn("jumlah");
        tbl.addColumn("total");
        tbl.addColumn("tunai");
        tbl.addColumn("kembalian");
        
        try {
            java.sql.Connection conn = mdl5.koneksi.koneksiDB();
            String sql = "select * from koneksimdl5";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
        
            while (rs.next()) {
                Object[] row = {rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4), 
                    rs.getString(5), rs.getString(6), 
                    rs.getString(7)
                };
                tbl.addRow(row);
            }
            
            tabeltransaksi.setModel(tbl);
            tabel.setModel(tbl);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
