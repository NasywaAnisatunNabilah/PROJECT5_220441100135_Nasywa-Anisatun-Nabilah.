    private void hitungtotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungtotalActionPerformed
        // TODO add your handling code here:
        if (kode.getText().isEmpty() || harga.getText().isEmpty() || jumlah.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Silahkan isi semua kolom terlebih dahulu:)");
            return;
        }
        try{
            int jumlah1 = Integer.parseInt(jumlah.getText());
            int harga1 = Integer.parseInt(harga.getText());
            int bayar = harga1 * jumlah1;
            total.setText(Integer.toString(bayar));
        }catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Masukkan angka yang valid");
        }
    }//GEN-LAST:event_hitungtotalActionPerformed

    private void hitungkembaliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungkembaliActionPerformed
        if (total.getText().isEmpty() || tunai.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Silahkan isi semua kolom terlebih dahulu:)");
            return;
        }
        try{
            int totalharga1 = Integer.parseInt(total.getText());
            int tunai1 = Integer.parseInt(tunai.getText());
            int kembali = tunai1 - totalharga1;
            if (tunai1 < totalharga1){
                JOptionPane.showMessageDialog(this, "Maaf nominal pembayaran anda kurang");
                tunai.setText("0");
            }else{
                kembalian.setText(Integer.toString(kembali));
            }
        }catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Masukkan angka yang valid");
        }
    }//GEN-LAST:event_hitungkembaliActionPerformed

